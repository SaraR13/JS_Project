                        <h2>About</h2>
                        <p>The world is an inspiring place. Its beauty is all around us and, sometimes, even what at first sight seems boring and normal, when interpreted by individuals and worked on, can be transformed onto something new and beautiful. <br><br>The artwork I create always starts from a picture - places I visit, places I live in, work at, objects in my house, streets, anywhere. I then edit those photographs to give it an illustration look, and finally I repeat the motif to create a pattern. I end up creating patterns of day-to-day objects, things that are real and which I transform into something surreal, in mosaics. Of some photographs I only edit the colour, contrast and so on, and then repeat to create a surreal space composed by mirrored images.</p>
