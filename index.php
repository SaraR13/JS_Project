                        <h2>About</h2>
                        <p>The world is an inspiring place! Its beauty is all around us. Sometimes what at first sight seems boring and normal, when interpreted by individuals and worked on, can be transformed into something new and beautiful. <br><br>
The artworks I create start with pictures of places I visit: places I live in, work at, objects in my house, streets or anywhere. I edit these photographs to give them an illustrated appearance, and repeat the motif to create a pattern. The patterns I create are of day-to-day objects, things that are real, and that I transform into the surreal mosaics. I edit colour, contrast and so on, repeating the process to create a surrealist space composed of mirrored images.</p>
